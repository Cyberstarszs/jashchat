    /* ===== Query parser + matcher logic ===== */
    function parseQuery(text) {
        const raw = text || '';
        const norm = normalizeStr(raw);
        // tokens keep words and numeric tokens separately
        const tokens = norm.split(' ').filter(t => t.trim().length);
        const numToken = tokens.find(t => /\d/.test(t));
        const qty = numToken ? parseNumberToken(numToken) : null;
        const platformDet = detectPlatformFromTokens(tokens);
        const platform = platformDet.platform;
        // detect unit words (likes/followers/views/duration)
        let unit = detectUnitFromText(norm);
        // also check tokens for duration words like 'bulan', 'minggu'
        const durationRegex = /(bulan|minggu|tahun|hari)/;
        const durationMatch = norm.match(durationRegex);
        if (durationMatch) unit = 'duration';
        return { raw, norm, tokens, qty, platform, unit, platformScore: platformDet.score };
    }

    function findMatches(parsed) {
        const results = [];
        const q = parsed;
        // if no dataset, refresh
        if (!SERVICE_DATA || SERVICE_DATA.length === 0) SERVICE_DATA = getServices();

        SERVICE_DATA.forEach(item => {
            // score components
            let platformScore = 0;
            if (q.platform) {
                platformScore = (normalizeStr(item.platform).includes(normalizeStr(q.platform)) || similarity(item.platform, q.platform) > 0.75) ? 1 : similarity(item.platform, q.platform);
            } else {
                // try to infer platform by similarity between item text and tokens
                platformScore = Math.max(...q.tokens.map(t => similarity(t, item.platform)));
            }

            let unitScore = 0;
            if (q.unit && item.unit) {
                unitScore = (q.unit === item.unit) ? 1 : similarity(q.unit, item.unit);
            } else {
                // try token-based match with item.name
                unitScore = Math.max(...q.tokens.map(t => similarity(t, item.name)));
            }

            let nameScore = similarity(q.norm, item.textLower);

            let qtyScore = 0;
            if (q.qty && item.qty) {
                const diff = Math.abs(item.qty - q.qty) / Math.max(1, q.qty);
                qtyScore = 1 - Math.min(diff, 1); // 1 = exact, 0 = very far
            } else if (q.qty && !item.qty) {
                qtyScore = 0.2; // small credit
            } else if (!q.qty && item.qty) {
                qtyScore = 0.4;
            } else {
                qtyScore = 0.25;
            }

            // weight combine
            // platform & unit are most important
            const score = (platformScore * 3) + (unitScore * 3) + (nameScore * 2) + (qtyScore * 2);
            // normalize to 0..1 (max possible = 10)
            const normScore = score / 10;
            results.push({ item, score: normScore, platformScore, unitScore, nameScore, qtyScore });
        });

        // sort descending
        results.sort((a, b) => b.score - a.score);
        return results;
    }

    /* Decide response based on parsed query + matches */
    function respondToQuery(text) {
        const p = parseQuery(text);
        const matches = findMatches(p);

        // if user asked only platform + unit (no qty) -> list available variants
        if (p.platform && p.unit && !p.qty) {
            const options = SERVICE_DATA.filter(it => similarity(it.platform, p.platform) > 0.6 && (it.unit === p.unit || similarity(it.unit || '', p.unit || '') > 0.6));
            if (options.length) {
                // aggregate unique name -> price
                const list = [];
                options.forEach(o => {
                    const display = `${o.name} ‚Äî ${o.price || 'Harga tdk tercantum'}`;
                    if (!list.includes(display)) list.push(display);
                });
                return `Pilihan ${p.platform} (${p.unit}):\n‚Ä¢ ` + list.slice(0, 10).join('\n‚Ä¢ ');
            }
        }

        // if qty given, try to find exact/nearest match
        if (p.qty) {
            // try top match with decent score
            const top = matches[0];
            if (top && top.score >= 0.55) {
                // prefer exact quantity if exists
                if (top.item.qty && top.item.qty === p.qty) {
                    return `${top.item.name} ${top.item.platform} = ${top.item.price}`;
                } else if (top.item.qty) {
                    // nearest but show suggestion/options
                    // find other close ones
                    const samePlat = SERVICE_DATA.filter(it => it.platform === top.item.platform && it.unit === top.item.unit && it.qty);
                    samePlat.sort((a, b) => Math.abs(a.qty - p.qty) - Math.abs(b.qty - p.qty));
                    const best = samePlat.slice(0, 3).map(it => `${it.name} ‚Äî ${it.price}`).join('\n‚Ä¢ ');
                    return `Tidak ada paket yang persis ${p.qty} pada ${top.item.platform} ${top.item.unit}. Pilihan terdekat:\n‚Ä¢ ${best}`;
                } else {
                    // no qty stored for top
                    return `${top.item.name} ${top.item.platform} = ${top.item.price}`;
                }
            } else {
                // fallback: search by qty across platforms for that unit if unit known
                const cross = SERVICE_DATA.filter(it => it.qty && Math.abs(it.qty - p.qty) <= Math.max(1, Math.round(p.qty * 0.4)));
                if (cross.length) {
                    const best = cross.slice(0, 6).map(it => `${it.name} (${it.platform}) ‚Äî ${it.price}`).join('\n‚Ä¢ ');
                    return `Saya tidak menemukan kecocokan pasti, tapi ini beberapa paket mirip:\n‚Ä¢ ${best}`;
                }
                return "Maaf, saya tidak menemukan harga yang cocok. Coba sebutkan platform (mis. tiktok/ig/youtube) atau periksa penulisan.";
            }
        }

        // if no qty and fuzzy match good -> show top match summary
        if (matches.length && matches[0].score >= 0.5) {
            const top = matches[0].item;
            // show some variants for that platform & unit
            const group = SERVICE_DATA.filter(it => it.platform === top.platform && (it.unit === top.unit || similarity(it.unit || '', top.unit || '') > 0.6));
            if (group.length > 1) {
                const list = group.slice(0, 6).map(g => `${g.name} ‚Äî ${g.price}`).join('\n‚Ä¢ ');
                return `Mungkin yang kamu maksud tentang ${top.platform}:\n‚Ä¢ ${list}`;
            } else {
                return `${top.name} ${top.platform} ‚Äî ${top.price}`;
            }
        }

        return "Maaf, saya tidak menemukan harga untuk itu. Coba sebutkan platform (mis: ig, tiktok, youtube) dan jumlah atau durasi (mis: 100 followers, 1 bulan).";
    }

