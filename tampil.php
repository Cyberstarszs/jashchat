    function detectPlatformFromTokens(tokens) {
        // exact or synonym match first
        for (const t of tokens) {
            for (const [plat, arr] of Object.entries(PLATFORM_MAP)) {
                if (arr.includes(t)) return { platform: plat, score: 1 };
            }
        }
        // fuzzy match
        let best = { platform: null, score: 0 };
        for (const t of tokens) {
            for (const [plat, arr] of Object.entries(PLATFORM_MAP)) {
                for (const variant of arr) {
                    const sim = similarity(t, variant);
                    if (sim > best.score) {
                        best = { platform: plat, score: sim };
                    }
                }
            }
        }
        // require reasonable similarity
        if (best.score >= 0.6) return best;
        return { platform: null, score: 0 };
    }

