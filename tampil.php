    function normalizeStr(s) {
        if (!s) return '';
        // lowercase, remove diacritics, punctuation (keep numbers and k)
        s = s.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
        s = s.replace(/[¬Æ‚Ñ¢‚Ä¢‚Ä¢,\/#!$%\^&\*;:{}=\_`~()?"'|<>+\[\]]/g, ' ');
        s = s.replace(/\s+/g, ' ').trim();
        return s;
    }

    function levenshtein(a, b) {
        if (a === b) return 0;
        if (!a.length) return b.length;
        if (!b.length) return a.length;
        let v0 = new Array(b.length + 1), v1 = new Array(b.length + 1);
        for (let i = 0; i < v0.length; i++) v0[i] = i;
        for (let i = 0; i < a.length; i++) {
            v1[0] = i + 1;
            for (let j = 0; j < b.length; j++) {
                let cost = a[i] === b[j] ? 0 : 1;
                v1[j + 1] = Math.min(v1[j] + 1, v0[j + 1] + 1, v0[j] + cost);
            }
            for (let j = 0; j < v0.length; j++) v0[j] = v1[j];
        }
        return v1[b.length];
    }

    function similarity(a, b) {
        if (!a || !b) return 0;
        a = normalizeStr(a); b = normalizeStr(b);
        const maxLen = Math.max(a.length, b.length);
        if (maxLen === 0) return 1;
        const dist = levenshtein(a, b);
        return 1 - (dist / maxLen);
    }

    /* Convert "1.000", "1k", "1.5k", "50" to number (1000, 1500, 50) */
    function parseNumberToken(tok) {
        if (!tok) return null;
        tok = tok.toLowerCase().replace(/\s/g, '');
        // if contains 'k'
        if (tok.includes('k')) {
            let num = parseFloat(tok.replace('k', '').replace(',', '.').replace(/\./g, ''));
            if (isNaN(num)) return null;
            return Math.round(num * 1000);
        }
        // remove thousand separators dots and commas
        let cleaned = tok.replace(/[.,]/g, '');
        let n = parseInt(cleaned, 10);
        return isNaN(n) ? null : n;
    }

