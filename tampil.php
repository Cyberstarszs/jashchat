    function parseTanggal(raw) {
        if (!raw) return { display: "-", sortKey: new Date(0) };

        raw = raw.replace(/^[:\s]+/, "").trim();
        const monthNames = [
            "Januari", "Februari", "Maret", "April", "Mei", "Juni",
            "Juli", "Agustus", "September", "Oktober", "November", "Desember"
        ];

        // 1. Format DD/MM/YYYY
        if (/^\d{1,2}\/\d{1,2}\/\d{4}/.test(raw)) {
            let [d, m, y] = raw.split(/[\/\s]/);
            let bulan = monthNames[parseInt(m) - 1] || m;
            return {
                display: `${d} ${bulan} ${y}`,
                sortKey: new Date(`${y}-${m.padStart(2, "0")}-${d.padStart(2, "0")}`)
            };
        }

        // 2. Format "Rabu, 27 Agustus 2025 10:45"
        let parts = raw.replace(",", "").split(" ");
        let onlyDate = parts.filter(p => !p.includes(":"));
        if (onlyDate.length >= 3) {
            if (isNaN(onlyDate[0])) onlyDate.shift(); // buang hari
            let [d, bulan, y] = onlyDate.slice(0, 3);
            let monthIndex = monthNames.indexOf(bulan);
            if (monthIndex >= 0) {
                return {
                    display: `${d} ${bulan} ${y}`,
                    sortKey: new Date(`${y}-${(monthIndex + 1).toString().padStart(2, "0")}-${d.padStart(2, "0")}`)
                };
            }
        }

        // 3. fallback ‚Üí tampilkan raw
        return { display: raw, sortKey: new Date(0) };
    }

