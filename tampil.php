            .then(data => {
                // Urutkan dari ID terbesar ke terkecil
                orders = data.sort((a, b) => {
                    // Convert ID ke number untuk sorting yang benar
                    const idA = parseInt(a.ID) || 0;
                    const idB = parseInt(b.ID) || 0;
                    return idB - idA; // Descending order
                });
                renderTable();
            })
            .catch(err => console.error("Gagal memuat data:", err));
    }

    function renderTable() {
        const tbody = document.querySelector("#orders-table tbody");
        tbody.innerHTML = "";

        let start = (currentPage - 1) * rowsPerPage;
        let end = start + rowsPerPage;
        let pageData = orders.slice(start, end);

        pageData.forEach(item => {
            const tr = document.createElement("tr");
            tr.innerHTML = `
                <td>${item.ID}</td>
                <td>${item.Tanggal}</td>
                <td>${item.Jenis}</td>
                <td>${item.Paket}</td>
                <td>${item.Total}</td>
                <td>${item.keterangan}</td>
            `;
            tbody.appendChild(tr);
        });

        // Update info halaman
        document.getElementById("pageInfo").textContent = 
            `Halaman ${currentPage} dari ${Math.ceil(orders.length / rowsPerPage)}`;
        
        // Enable/disable tombol navigasi
        document.getElementById("prevPage").disabled = currentPage === 1;
        document.getElementById("nextPage").disabled = currentPage === Math.ceil(orders.length / rowsPerPage);
    }

    // Event listeners untuk pagination
    document.getElementById("prevPage").addEventListener("click", () => {
        if (currentPage > 1) {
            currentPage--;
            renderTable();
        }
    });

    document.getElementById("nextPage").addEventListener("click", () => {
        if (currentPage < Math.ceil(orders.length / rowsPerPage)) {
            currentPage++;
            renderTable();
        }
    });

    function animateCounter(element, target, duration = 2000) {
        let start = 0;
        let stepTime = Math.max(Math.floor(duration / target), 20);
        let timer = setInterval(() => {
            start++;
            element.textContent = start.toLocaleString("id-ID");
            if (start >= target) clearInterval(timer);
        }, stepTime);
    }

    async function loadStats() {
        try {
            const res = await fetch("hasil_transaksi.json");
            let data = await res.json();

            let totalOrders = data.length;
            let kepuasan = Math.max(0, totalOrders - Math.floor(Math.random() * 5 + 3));

            animateCounter(document.getElementById("totalOrders"), totalOrders);
            animateCounter(document.getElementById("kepuasan"), kepuasan);
        } catch (err) {
            console.error("Gagal memuat statistik:", err);
        }
    }

    loadOrders();
    loadStats();
    document.querySelectorAll('#kuota .service-submenu button').forEach(btn => {
  btn.addEventListener('click', () => {
    let parent = btn.closest('.service-content')
    parent.querySelectorAll('.service-submenu button').forEach(b => b.classList.remove('active'))
    btn.classList.add('active')
    let target = btn.getAttribute('data-sub')
    parent.querySelectorAll('.sub-content').forEach(card => {
      card.classList.remove('active')
      if (card.id === target) {
        card.classList.add('active')
      }
    })
  })
})
